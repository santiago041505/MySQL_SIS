    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        int rowIndex = studentTable.getSelectedRow();
        
        if (rowIndex >= 0) {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) studentTable.getModel();
            
            
            selectedStudentId = Integer.parseInt(model.getValueAt(rowIndex, 0).toString());
            
            
            txtFirstName.setText(model.getValueAt(rowIndex, 1).toString());
            txtLastName.setText(model.getValueAt(rowIndex, 2).toString());
            txtAge.setText(model.getValueAt(rowIndex, 3).toString());
            txtEmail.setText(model.getValueAt(rowIndex, 4).toString());
        }
    }//GEN-LAST:event_studentTableMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedStudentId == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first.");
            return;
        }

        try {
            Student student = new Student();
            student.setStudentId(selectedStudentId); // Use the clicked ID!
            student.setFirstName(txtFirstName.getText());
            student.setLastName(txtLastName.getText());
            student.setAge(Integer.parseInt(txtAge.getText()));
            student.setEmail(txtEmail.getText());

            studentDAO.updateStudent(student);
            javax.swing.JOptionPane.showMessageDialog(this, "Student Updated Successfully!");
            
            loadTable(); // Refresh the table to show the new data
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       if (selectedStudentId == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first.");
            return;
        }

        // Ask for confirmation before deleting
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Warning", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            studentDAO.deleteStudent(selectedStudentId);
            javax.swing.JOptionPane.showMessageDialog(this, "Student Deleted Successfully!");
            
            loadTable(); // Refresh the table
            selectedStudentId = -1; // Reset the selected ID
            
            // Clear the text fields
            txtFirstName.setText("");
            txtLastName.setText("");
            txtAge.setText("");
            txtEmail.setText("");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
private void setupTable() {
        // This sets the column names for your table
        studentTable.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {"ID", "First Name", "Last Name", "Age", "Email"}
        ));
    }

    private void loadTable() {
        // This clears the table and loads the fresh data from the database
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) studentTable.getModel();
        model.setRowCount(0); // Clear existing rows

        java.util.ArrayList<Student> list = studentDAO.getAllStudents();
        for (Student s : list) {
            Object[] row = {
                s.getStudentId(),
                s.getFirstName(),
                s.getLastName(),
                s.getAge(),
                s.getEmail()
            };
            model.addRow(row);
        }}
