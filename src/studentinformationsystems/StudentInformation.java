    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       loadTable();
        try {
            // 1. Create a new Student object
            Student student = new Student();
            
            // 2. Get the text from the text fields and put it in the object
            student.setFirstName(txtFirstName.getText());
            student.setLastName(txtLastName.getText());
            student.setAge(Integer.parseInt(txtAge.getText())); // Convert text to number
            student.setEmail(txtEmail.getText());

            // 3. Send the object to your DAO to save it to the database
            studentDAO.addStudent(student);

            // 4. Show a success popup
            javax.swing.JOptionPane.showMessageDialog(this, "Student Added Successfully!");

        } catch (NumberFormatException e) {
            // This catches the error if the user types a word instead of a number for Age
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

